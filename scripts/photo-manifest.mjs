/**
 * Scans public/photos/ and writes src/lib/photo-manifest.ts — the list of
 * seeds that have a real image file. Runs automatically before every build
 * and dev start (see package.json "prephoto"/"predev"), so dropping a file
 * named <seed>.<ext> into public/photos/ makes it appear on the site with
 * no code changes.
 *
 * Supported extensions (first match wins): jpg, jpeg, png, webp, avif.
 */
import { readdirSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, parse } from "node:path";

const PHOTOS_DIR = join(process.cwd(), "public", "photos");
const OUT = join(process.cwd(), "src", "lib", "photo-manifest.ts");
const EXT_PRIORITY = ["avif", "webp", "jpg", "jpeg", "png"];

if (!existsSync(PHOTOS_DIR)) mkdirSync(PHOTOS_DIR, { recursive: true });

const files = readdirSync(PHOTOS_DIR).filter((f) => !f.startsWith("."));

// Map seed -> best available "/photos/<file>" public path.
const bySeed = {};
for (const file of files) {
  const { name, ext } = parse(file);
  const e = ext.replace(".", "").toLowerCase();
  if (!EXT_PRIORITY.includes(e)) continue;
  const seed = name.toLowerCase();
  const current = bySeed[seed];
  if (!current || EXT_PRIORITY.indexOf(e) < EXT_PRIORITY.indexOf(current.ext)) {
    bySeed[seed] = { path: `/photos/${file}`, ext: e };
  }
}

const entries = Object.entries(bySeed)
  .map(([seed, { path }]) => `  ${JSON.stringify(seed)}: ${JSON.stringify(path)},`)
  .sort()
  .join("\n");

const out = `// AUTO-GENERATED by scripts/photo-manifest.mjs — do not edit by hand.
// Maps a photo "seed" to its file in public/photos/. Drop <seed>.jpg (or
// .png/.webp/.avif) into public/photos/ and rebuild to populate this.
export const PHOTO_MANIFEST: Record<string, string> = {
${entries}
};
`;

writeFileSync(OUT, out, "utf8");
const count = Object.keys(bySeed).length;
console.log(`[photo-manifest] ${count} photo${count === 1 ? "" : "s"} found in public/photos/`);
